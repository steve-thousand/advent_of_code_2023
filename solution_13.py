def is_mirror_y(map, y, require_smudge=False):
    d = 1
    unequal = 0
    while y - d + 1 >= 0 and y + d < len(map):
        for x in range(0, len(map[y])):
            unequal += 1 if map[y - d + 1][x] != map[y + d][x] else 0
        d += 1
    return unequal == (1 if require_smudge else 0)

def is_mirror_x(map, x, require_smudge=False):
    d = 1
    unequal = 0
    while x - d + 1 >= 0 and x + d < len(map[0]):
        for y in range(0, len(map)):
            unequal += 1 if map[y][x - d + 1] != map[y][x + d] else 0
        d += 1
    return unequal == (1 if require_smudge else 0)

def score(map, require_smudge=False):
    for y in range(0, len(map) - 1):
        if is_mirror_y(map, y, require_smudge):
            return 100 * (y + 1)
    for x in range(0, len(map[0]) - 1):
        if is_mirror_x(map, x, require_smudge):
            return x + 1

def solve(puzzle_input):
    maps = [[[x for x in y] for y in map.split("\n")] for map in puzzle_input.strip().split("\n\n")]

    part_1 = 0
    for map in maps:
        part_1 += score(map)
    print(part_1)

    part_2 = 0
    for map in maps:
        part_2 += score(map, True)
    print(part_2)

