def calculate_load(grid):
    load = 0
    for y in range(0, len(grid)):
        for x in range(0, len(grid[0])):
            if grid[y][x] == 'O':
                load += len(grid) - y
    return load

def tilt_north(grid, reverse=False):
    for x in range(0, len(grid[0])):
        for y in range(0, len(grid)) if not reverse else range(len(grid) - 1, -1, -1):
            if grid[y][x] == 'O':
                for new_y in range(y - 1, -2, -1) if not reverse else range(y + 1, len(grid) + 1, 1):
                    if new_y == (-1 if not reverse else len(grid)) or grid[new_y][x] in "O#":
                        grid[y][x] = '.'
                        grid[new_y + (1 if not reverse else -1)][x] = 'O'
                        break

def tilt_south(grid):
    tilt_north(grid, True)

def tilt_west(grid, reverse=False):
    for y in range(0, len(grid)):
        for x in range(0, len(grid[0])) if not reverse else range(len(grid[0]) - 1, -1, -1):
            if grid[y][x] == 'O':
                for new_x in range(x - 1, -2, -1) if not reverse else range(x + 1, len(grid[0]) + 1, 1):
                    if new_x == (-1 if not reverse else len(grid[0])) or grid[y][new_x] in "O#":
                        grid[y][x] = '.'
                        grid[y][new_x + (1 if not reverse else -1)] = 'O'
                        break

def tilt_east(grid):
    tilt_west(grid, True)

def solve(puzzle_input):
    grid = [[x for x in y] for y in puzzle_input.strip().split("\n")]
    tilt_north(grid)
    print(calculate_load(grid))

    grid = [[x for x in y] for y in puzzle_input.strip().split("\n")]
    state_map = {}
    final_state = None
    for i in range(1, 1_000_000_001):
        tilt_north(grid)
        tilt_west(grid)
        tilt_south(grid)
        tilt_east(grid)
        state = '\n'.join([''.join(y) for y in grid])
        if state in state_map:
            prev_i = state_map[state]
            period = i - prev_i
            period_i = (1_000_000_000 - i) % period + prev_i
            final_state = next(key for key, value in state_map.items() if value == period_i)
            break
        else:
            state_map[state] = i

    grid = [[x for x in y] for y in final_state.strip().split("\n")]
    print(calculate_load(grid))

    return

